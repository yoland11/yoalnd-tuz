export function translate(locale: Locale, ar: string): string {
  if (locale === "ar") return ar;
  return translations[locale]?.[ar] ?? ar;
}

export function useLocale() {
  const [locale, setLoc] = useState<Locale>(() => readLocale());

  useEffect(() => {
    const sync = () => setLoc(readLocale());
    sync();
    window.addEventListener(CHANGE_EVENT, sync);
    window.addEventListener("storage", sync);
    return () => {
      window.removeEventListener(CHANGE_EVENT, sync);
      window.removeEventListener("storage", sync);
    };
  }, []);

  return { locale, setLocale, dir: localeDir(locale) };
}

/** خطّاف الترجمة: t("النص العربي") */
export function useT() {
  const { locale } = useLocale();
  return (ar: string) => translate(locale, ar);
}
